"use client";
import { useSignIn } from "@clerk/nextjs";
import { Button } from "@nextui-org/react";

export default function SignInOAuthButtons() {
  const { signIn, isLoaded } = useSignIn();
  if (!isLoaded) {
    return null;
  }
  const signInWith = (strategy) => {
    return signIn.authenticateWithRedirect({
      strategy: strategy,
      redirectUrl: "/sso-callback",
      redirectUrlComplete: "/",
    });
  };

  return (
    <div className="flex flex-row gap-4">
      <Button
        onClick={() => signInWith("oauth_discord")}
        className="w-full h-16 text-medium hover:bg-zinc-700"
        variant="bordered"
        startContent={<DiscordLogo />}
      >
        Discord
      </Button>
      <Button
        onClick={() => signInWith("oauth_google")}
        className="w-full h-16 text-medium hover:bg-zinc-700"
        variant="bordered"
        startContent={<GoogleLogo />}
      >
        Google
      </Button>
    </div>
  );
}

